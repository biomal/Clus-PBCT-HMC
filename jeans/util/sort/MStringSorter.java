package jeans.util.sort;

public class MStringSorter implements MComparator {

	protected static MStringSorter m_hInstance;

	public static MStringSorter getInstance() {
		if (m_hInstance == null) m_hInstance = new MStringSorter();
		return m_hInstance;
	}

	public int compare(Object obj1, Object obj2) {
		String n1 = obj1.toString();
		String n2 = obj2.toString();
		return n2.compareTo(n1);
	}
}
